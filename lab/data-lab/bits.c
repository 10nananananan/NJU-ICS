/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
/* if x<0 and y>0,then a=0xFFFFFFFF,else a=0x0
   if x>0 and y<0,then b=0xFFFFFFFF,else b=0x0
   if y-x>0,then c=0xFFFFFFFF,else c=0x0
*/
	int a=(x>>31)&~(y>>31);
	int b=~(x>>31)&(y>>31);
	int c=~((y+~x+1)>>31);
	return ~b&0x1&(a|c);
}
/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
/* 2^(highbit+1)-2^lowbit */
	int a=~((highbit+~lowbit+1)>>31);
	int b=(0x1<<highbit<<1)+~(0x1<<lowbit)+1;
	return a&b;
}
/* 
 * float_half - Return bit-level equivalent of expression 0.5*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_half(unsigned uf) {
/* if uf is infinity,then result=infinity
   if uf is normalized,then exponent--
   if uf's exponent is 1,it should be paid attention
   if uf is unnormalized,then fraction/2
*/
	int a=uf>>31<<31;
	int b=(uf>>23)&0xFF;
	int c=uf&0x7FFFFF;
	unsigned result=0;
	if(b==0xFF)
		result=uf;
	else if(b>0x1)
		result=a|((b-1)<<23)|c;
	else if(b==0x1)
		result=a|(((c|0x800000)>>1)+(c&(c>>1)&0x1));
	else
		result=a|((c>>1)+(c&(c>>1)&0x1));
	return result;
}
