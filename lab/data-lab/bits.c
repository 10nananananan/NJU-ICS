/* 
 * sm2tc - Convert from sign-magnitude to two's complement
 *   where the MSB is the sign bit
 *   Example: sm2tc(0x80000005) = -5.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int sm2tc(int x) {
/* if x==0,then c=0x0,else c=0xFFFFFFFF
   if x>0,then a=0,result=x
   if x<0,then b=0,result=x's two's complement
*/
	int y=x>>31;
	int z=!!(x<<1);
	int a=y&((~x+1)|(0x1<<31));
	int b=~y&x;
	int c=~z+1;
	return c&(a|b);
}
/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
/* divide x into sign,fraction and exponent
   shift left till the second highest bit is 1 and calculate the exponent
   round according GRS
   if carry happens,then exponent adds 1 and fraction shifts right
*/
	int sign=(x>>31)&0x1;
	int fraction=0;
	int exponent=157;
	unsigned result=0;
	int y=0;
	fraction=sign?-x:x;
	while(exponent>126&&!(fraction>>30))
	{
		fraction<<=1;
		exponent--;
	}
	if(exponent<127)
		exponent=0;
	y=fraction&0x70;
	if(y>0x40||(y==0x40&&((fraction&0x80)==0x80||fraction&0xF)))
		fraction+=0x80;
	if(fraction>>31)
	{
		fraction>>=1;
		exponent++;
	}
	fraction=(fraction>>7)&0x7FFFFF;	
	result=(sign<<31)|(exponent<<23)|fraction;
	return result;
}
	
