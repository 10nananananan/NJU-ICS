/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
/* add 32 bits by select appropriate netmask */
	int x1=(0x55<<8)|0x55;
	int y1=(x1<<16)|x1;
	int x2=(0x33<<8)|0x33;
	int y2=(x2<<16)|x2;
	int x3=(0xF<<8)|0xF;
	int y3=(x3<<16)|x3;
	int y4=(0xFF<<16)|0xFF;
	int y5=(0xFF<<8)|0xFF;
	int a=(x&y1)+((x>>1)&y1);
	int b=(a&y2)+((a>>2)&y2);
	int c=(b&y3)+((b>>4)&y3);
	int d=(c&y4)+((c>>8)&y4);
	int e=(d&y5)+((d>>16)&y5);
	return e;
}
/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
/* x and its two's complement have the same leastBitPos */
   return (~x+1)&x;
}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateLeft(int x, int n) {
/* the n high bits shfit right,the 32-n low bits shfit left */
	int a=x>>(33+~n);
	int b=(0x1<<n)+~0x1+1;
	int c=x<<n;
	return (a&b)|c;
}
